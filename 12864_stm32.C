void judge_psb(void)
{
    u8 ad1,ad2;
	//PSB_CH();
	GPIO_Configuration();
	LCD_PSB_L;delay_us(1);				//假设PSB为0（串口），延时一下
	GPIO_Configuration_PSB();			//读
	if(GPIO_ReadInputDataBit(GPIOB,GPIO_Pin_9)==Bit_SET)ad1=1 ; else ad1=0;
	//ad1=PSB;						//读PSB的值，
	if(ad1==0)						//假设是0，读出来还是0，不代表就是0，因为如果是个悬空时也是0
	{
		GPIO_Configuration();
		LCD_PSB_H;delay_us(1);			//再假设高电平（并口），延时一下（防止误操作）
		GPIO_Configuration_PSB();
		if(GPIO_ReadInputDataBit(GPIOB,GPIO_Pin_9)==Bit_SET)ad2=1 ; else ad2=0;
		//ad2=PSB;					//读PSB值 
		if(ad2==1)					//第2次假设为高电平也能读出高电平（高低是可控，说明没有固定通迅方式）
		{
			if(PSB_in==1) {GPIO_Configuration();LCD_PSB_H;PSB_CC=4;}//按程序的配置1为并口，0为串口 
			else {GPIO_Configuration();LCD_PSB_L;PSB_CC=3;}
		}
		else {GPIO_Configuration();LCD_PSB_L;PSB_CC=1;}		
	}
	else {GPIO_Configuration();LCD_PSB_H;PSB_CC=2;}		//如果假设0,读出来是1，说明是并口，CC=2	
}


//初始化LCD-8位接口
void LCDInit(void)
{ 	
	judge_psb();	//检测PSB状态，配置高低电平
	
    LCD_RST_H;
    LCD_RST_L;
	delay_ms(40);
    LCD_RST_H;		//硬复位
	delay_ms(1);
	if(M_68==4)	WRCommandH(0x20);
	else 		WRCommandH(0x30);
  	delay_ms(1);	//基本指令集,8/4位
	
	WRCommandH(0x06);delay_ms(1);	//启始点设定：光标右移
	WRCommandH(0x01);delay_ms(11);	//清除显示DDRAM
	WRCommandH(0x0C);delay_ms(1);	//显示状态开关：整体显示开，光标显示关，光标显示反白关
	WRCommandH(0x02);delay_ms(1);//地址归零		
}

